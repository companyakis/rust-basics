fn main() {

    //min and max values of integer types

    //examples

    let min_val_i16 = i16::MIN;

    let max_val_16 = i16::MAX;

    let min_val_u32 = u32::MIN;

    let max_val_u32 = u32::MAX;

    println!("min_val_i16 : {min_val_i16}");
    println!("max_val_16 : {max_val_16}" );
    println!("min_val_u32 : {min_val_u32}");
    println!("max_val_u32 : {max_val_u32}" );

}

/*
min_val_i16 : -32768
max_val_16 : 32767
min_val_u32 : 0
max_val_u32 : 4294967295

*/
